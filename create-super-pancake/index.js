  console.log(`Screenshot options: ${screenshotOptions.join(', ')}`);
  config.screenshot = await askQuestion(`${colors.bright}Screenshots${colors.reset}`, 'only-on-failure');
  
  const videoOptions = ['always', 'retain-on-failure', 'off'];
