async function runTestSuite(suite) {
  console.log(`\nğŸ§ª Running: ${suite.name}`);
  console.log(`ğŸ“ ${suite.description}`);
  console.log(`ğŸ“‚ Path: ${suite.path}`);
  console.log('â”€'.repeat(60));

  // Build vitest command with exclusions
  const vitestArgs = ['vitest', 'run', suite.path, '--reporter=verbose'];
  
  // Skip ui-workflow.test.js during CI/publishing
  if (process.env.CI || process.env.NODE_ENV === 'production') {
    console.log('âš ï¸ CI/Production mode detected - excluding ui-workflow.test.js');
  }

  return new Promise((resolve) => {
    const child = spawn('npx', vitestArgs, {
      stdio: 'inherit',
      shell: true
    });

    child.on('exit', (code) => {
      const status = code === 0 ? 'âœ… PASSED' : 'âŒ FAILED';
      console.log(`${status}: ${suite.name}`);
      resolve({ name: suite.name, success: code === 0, code });
    });
  });
}

